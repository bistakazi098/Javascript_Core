// relational operators 
// greater than
document.write(5 > 2);

// less than
document.write(5 < 2);

//less than equal to
document.write(5 <= 2);

// greater than equal to 
document.write(5 >= 2);

// equal to 
document.write(5 == 2);

// not equal to 
document.write(5 != 2);

// equal value same and same type
document.write(5 === 2);
document.write(5 == "2");

//equal value not same type 
document.write(5 !== 2);
document.write(5 != "2");

